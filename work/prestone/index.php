		<section id="abellio-intro" class="main-container">
			<div class="flexbox listen">
				<h2>Listen</h2>
				<div class="listen-copy">
					<div class="rule">
						<img src="/img/rule.png" alt="Rule" />
					</div>
					<p>Despite supplying car care products to the global automotive market for almost a century, Holt Lloyd International’s Prestone brand had a low profile in the UK.</p>
					<p>Fighting for shelf space in an already overcrowded sector, Holts knew it needed something big to make its range of coolants and screenwash stand out from their competitors.</p>
					<p>They decided on sponsorship and the MSA British Rally Championship seemed like the perfect fit. As one of the largest spectator sports in the UK, the competition is synonymous with speed, performance and conditions that test drivers to the extremes.</p>
					<p>The problem? The sponsorship deal had been done, but how were they going to activate it? They asked The Foundry to help them leverage the position of Prestone and make it the must-have car care brand for the British motorist. 
					</p>
				</div>	
			</div>
			<hr />
			<div class="flexbox plan">
				<h2>Plan</h2>
				<div class="plan-copy">
					<div class="rule">
						<img src="/img/rule.png" alt="Rule" />
					</div>
					<p>Our task was to create a buzz about the brand and drive spectator engagement at each of the seven stages of the competition in Scotland, England, Wales, Northern Ireland, Belgium and the final in The Isle of Man.</p>
					<p>We began with branding, creating a dynamic logo lockup that gave the Prestone and BRC partnership an identity of its own. This was used across all marketing materials and printed on giveaways to increase visibility on race days.</p>
					<p>The new logo appeared on gazebos, banners, flags, race vehicles, clothing, caps and rain ponchos, which were welcomed by spectators in inclement weather during the first leg of the series in the Scottish borders.</p>
					<p>To further involve motorsport fans, we set up a rally game simulator at each event where spectators could test their driving skills on a virtual track. The fastest drivers won a day at a rally driving school, with a VIP experience at Rally GB for the overall winner at the end of the championship.</p>
					<p>As well as creating extra excitement on the day, the gamification proved to be excellent for data capture, working in tandem with the Prestone microsite which is home to race information, leaderboards and driver profile videos. We also produced consumer ads to appear in the race programme distributed at events.</p>
					<p>Finally, the new joint branding appeared on 5’, 10’ and 15’ second break bumpers at the start, middle and end of The British Rally Championship Highlights on Channel 4 and BT Sport, ensuring Prestone was seen by an even wider audience.</p>
				</div>	
			</div>
			<hr />
			<div class="flexbox deliver">
				<h2>Deliver</h2>
				<div class="deliver-copy">
					<div class="rule">
						<img src="/img/rule.png" alt="Rule" />
					</div>
					<p>At the end of Round 1, we’re already beginning to see the impact of this brand activation programme.</p>
					<p>During February and March, BRC social accounts reached over 39 million people, with Prestone’s targeted content reaching 132,000 fans. The new, dual-branded MSABRC website has had 119,493 unique users since the start of the year with 18,000 video views.</p> 
					<p>Newspaper and online reach totalled 11,877,557 and our coverage in local press was seen by almost 8 million readers. At the events, we gave away 1000 bottles of screen wash, 650 bottles of coolant, 1000 branded caps and 500 ponchos, making the Prestone brand visible to thousands more.</p>
					<p>Holt Lloyd International set themselves a target of increasing brand awareness by 4% in year one. After the first leg in the series, recognition of the Prestone brand has already risen by 2%! We’ll be able to report more as the British Rally Championship unfolds. Watch this space!</p>
				</div>
